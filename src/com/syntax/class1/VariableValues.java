package com.syntax.class1;

public class VariableValues {
	public static void main(String [] args) {
		byte variable1=119;
		short variable2=24664;
		int variable3=219486384;
		long variable4=8454567738483475897l;
		System.out.println(variable1);
		System.out.println(variable2);
		System.out.println(variable3);
		System.out.println(variable4);
		
		float variable5=2674.85695868f;
		double variable6=45899.675654854368;
		System.out.println(variable5);
		System.out.println(variable6);
		
		char variable7='Y';
		System.out.println(variable7);
		
		boolean injured=true;
		System.out.println(injured);
		
		
	}

}
